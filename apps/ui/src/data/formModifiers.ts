export const DEFAULT_FORM_STATE: FormStateId = 'BASE';

export function applyFormMultiplier(
  value: number | undefined,
  field: StatKey,
  state: FormStateId,
): number | undefined {
  if (value === undefined || Number.isNaN(value)) return undefined;
  const factor = FORM_MULTIPLIERS[state]?.[field] ?? 1;
  return value * factor;
}
